
public class switchDemo {

	public static void main(String[] args) {
	  
		char grade ='C';
		
		switch(grade){
		case 'A':
		System.out.println("Mükemmel : Geçtiniz");		
		break;
		case 'B':
		System.out.println("İyi : Geçtiniz");		
			break;
		case 'C':
		System.out.println("Fena değil : Geçtiniz");		
			break;
		case 'D':
		System.out.println("Kötü : Kaldınız");		
			break;
		
		
		
		}
		
		

	}

}
